CREATE FORCE VIEW "PUBLIC"."BDE_PES"("REGIME", "PES_ID", "BDE_ID", "TIPO", "COMPROVANTE_PJ", "TIPO_DECLARANTE") AS
SELECT
    "CODIGO_RECEITA"."REGIME",
    "BENEFICIARIO_DECLARANTE"."PES_ID",
    "BENEFICIARIO_DECLARANTE"."BDE_ID",
    "PESSOA"."TIPO",
    "CODIGO_RECEITA"."COMPROVANTE_PJ",
    "DIRFS"."IN_PF_PJ"
FROM "PUBLIC"."CODIGO_RECEITA"
INNER JOIN "PUBLIC"."BENEFICIARIO_DECLARANTE"
    ON 1=1
INNER JOIN "PUBLIC"."PESSOA"
    ON 1=1
INNER JOIN "PUBLIC"."DIRFS"
    ON 1=1
WHERE ("BENEFICIARIO_DECLARANTE"."DIR_ID" = "DIRFS"."DIR_ID")
    AND (("PESSOA"."PES_ID" = "BENEFICIARIO_DECLARANTE"."PES_ID")
    AND ("BENEFICIARIO_DECLARANTE"."CDR_ID" = "CODIGO_RECEITA"."CDR_ID"));
	
